public class constants {

    public static void main(String[] args) {

        // To prevent certain fixed values from being overriden, we use constants.
        // Constants are variables whose values cannot be changed.

        // To declare a constant, we use the keyword final.
        final double PI = 3.14;

        System.out.println(PI);

        // PI = 1; // This will result in an error.

        // We can't change the value of a constant.



    }
}
