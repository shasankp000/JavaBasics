
public class TypeCasting {

    public static void main(String[] args) {

        // Typecasting : Changing the type of variables
        // int -> double
        // int -> float
        // int -> long
        // int -> char
        // int -> short

        // Types : There are two types, primitives, for storing simple values and reference types, for storing complex values.
        // Primitive types : byte, int, double, float, long, char, short
        // Reference types : String, array, object

        // Type



        int a = 10;
        int b = 20;
        double c = 30.5;

        System.out.println(a + b + c);

        byte age = 30;

        System.out.println("Age: " + age);


        // long integer

        long largeNumber = 123_456_789L;
        System.out.println("Really large number: " + largeNumber); // 123456789

        // floating-point number

        float float_number = 10.99F;

        System.out.println("Floating point number: " + float_number);

        // boolean

        boolean is_true = true;

        System.out.println("Is 2+2=4? " + is_true);

        // char

        char first_letter = 'A';

        System.out.println("Just a character: " + first_letter);

    }
}
